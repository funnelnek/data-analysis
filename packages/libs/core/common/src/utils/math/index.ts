/**
 * Multiply a number by 100.
 * 
 * The purpose is to convert a float ponit less than or equal to 1 into percentage.
 * @param {number} num - The the number to multiply by 100.
 * @returns {number} - The number multipied by 100.
 */
export function percentage(num: number): number {
    return num * 100;
}